<div class="row col-m-11 col-m-padding-down-2">
    <h3>Comment ajouter une page à votre site ?</h3><br />

    <div class="shadow-box-square">
        <p class="text-faq">Il existe plusieurs façons d'ajouter une page à votre site :</p>
        <ul>
            <li>Sur la page "pages" cliquez sur le bouton "ajouter" et renseignez les informations sur votre page</li>
            <li>Sur la page "apparence" renseignez les informations de votre nouvelle page et ajoutez-y directement du contenu</li>
        </ul>
    </div>

    <h3>Comment changer mon mot de passe</h3><br />

    <div class="shadow-box-square">
        <p class="text-faq">Cliquez sur votre nom en haut à droite de votre écran, vous accèderez aux réglages de votre profil.<br />
            Vous pourrez changer votre mot de passe dans l'onglet "Réglages".
        </p>
    </div>

    <h3>Comment ajouter un article à mon site ?</h3><br />

    <div class="shadow-box-square">
        <p class="text-faq">Cliquez sur l'onglet "Articles" dans le menu de gauche, cliquez ensuite sur "+ ajouter un article"</p>
    </div>

    <h3>Comment modifier un article ?</h3><br />

    <div class="shadow-box-square">
        <p class="text-faq">Pour modifier un article cliquez sur l'onglet "Articles" vous verrez votre article dans le tableau, il vous suffit de cliquer
        sur le crayon pour modifier votre article</p>
    </div>
</div>

